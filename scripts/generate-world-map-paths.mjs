#!/usr/bin/env node
/**
 * Generate src/ui/widgets/data/worldMapPaths.ts from Natural Earth 110m land outlines.
 *
 * Source: https://github.com/martynafford/natural-earth-geojson (public domain)
 *         110m/physical/ne_110m_land.json
 *
 * Output: array of SVG path-d strings projected to equirectangular coordinate space
 * spanning [0, 360] (x = longitude + 180) by [0, 180] (y = 90 - latitude).
 *
 * Run: node scripts/generate-world-map-paths.mjs
 */
import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const outputPath = path.join(repoRoot, "src", "ui", "widgets", "data", "worldMapPaths.ts");

const SOURCE_URL =
  "https://raw.githubusercontent.com/martynafford/natural-earth-geojson/master/110m/physical/ne_110m_land.json";

const PROJECTED_WIDTH = 360;
const PROJECTED_HEIGHT = 180;

function project(lng, lat) {
  const x = lng + 180;
  const y = 90 - lat;
  return [
    Math.max(0, Math.min(PROJECTED_WIDTH, x)),
    Math.max(0, Math.min(PROJECTED_HEIGHT, y))
  ];
}

function ringToPath(ring) {
  if (ring.length < 3) {
    return null;
  }
  const segments = [];
  let i = 0;
  for (const [lng, lat] of ring) {
    const [x, y] = project(lng, lat);
    segments.push(`${i === 0 ? "M" : "L"}${x.toFixed(2)},${y.toFixed(2)}`);
    i += 1;
  }
  segments.push("Z");
  return segments.join(" ");
}

function geometryToPaths(geometry) {
  const paths = [];
  if (geometry.type === "Polygon") {
    for (const ring of geometry.coordinates) {
      const path = ringToPath(ring);
      if (path) paths.push(path);
    }
  } else if (geometry.type === "MultiPolygon") {
    for (const polygon of geometry.coordinates) {
      for (const ring of polygon) {
        const path = ringToPath(ring);
        if (path) paths.push(path);
      }
    }
  }
  return paths;
}

async function main() {
  console.log(`Fetching ${SOURCE_URL}...`);
  const response = await fetch(SOURCE_URL);
  if (!response.ok) {
    throw new Error(`Failed to fetch source GeoJSON: ${response.status} ${response.statusText}`);
  }
  const geojson = await response.json();
  const allPaths = [];
  for (const feature of geojson.features ?? []) {
    if (!feature.geometry) continue;
    allPaths.push(...geometryToPaths(feature.geometry));
  }
  const lines = [
    "// Generated by scripts/generate-world-map-paths.mjs from Natural Earth 110m land.",
    "// Coordinate space is equirectangular [0..360, 0..180] where x = lng + 180, y = 90 - lat.",
    "// Source: https://github.com/martynafford/natural-earth-geojson (public domain)",
    "// Do not edit by hand; rerun the generator to refresh.",
    "",
    `export const WORLD_MAP_VIEWBOX = "0 0 ${PROJECTED_WIDTH} ${PROJECTED_HEIGHT}" as const;`,
    `export const WORLD_MAP_WIDTH = ${PROJECTED_WIDTH};`,
    `export const WORLD_MAP_HEIGHT = ${PROJECTED_HEIGHT};`,
    "",
    "export const WORLD_MAP_PATHS: string[] = ["
  ];
  for (const path of allPaths) {
    lines.push(`  ${JSON.stringify(path)},`);
  }
  lines.push("];", "");
  writeFileSync(outputPath, lines.join("\n"), "utf8");
  console.log(`Wrote ${allPaths.length} paths → ${path.relative(repoRoot, outputPath)}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
